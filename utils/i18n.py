def tr(text: str) -> str:
    if not is_english():
        return text
    return UI_EN.get(text, text)


def localize_html(html: str) -> str:
    if not is_english():
        return html
    for zh, en in sorted(HTML_EN.items(), key=lambda item: len(item[0]), reverse=True):
        html = html.replace(zh, en)
    return html


def current_language() -> str:
    return get_language()
